#include "libft.h"

int	ft_isalnum(int c)
{
	if (c == EOF)
		return (FALSE);
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z') || (c >= 'a'
			&& c <= 'z'))
		return (TRUE);
	return (FALSE);
}
