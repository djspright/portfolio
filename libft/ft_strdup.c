#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		len;
	char	*dst;

	if (!s)
		return (NULL);
	i = 0;
	len = ft_strlen(s);
	dst = (char *)malloc((len + 1) * sizeof(char));
	if (dst == NULL)
		return (NULL);
	while (s[i])
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
